package kr.co.test;

public class test06 {

	public static void main(String[] args) {
		//문자형 이해하기
		char ch = 'a';
		System.out.println("문자 ch=>"+ch);
		System.out.printf("문자 ch=> %c",ch);
		System.out.println();
		System.out.println("문자 (int)ch=>"+(int)ch);
		System.out.printf("문자 ch=> %5d",(int)ch); //%는 포멧 잡아준다
		
		System.out.printf("%d",ch);
		
		
	}

}
